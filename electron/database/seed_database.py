def main():
    """Main function to connect to the DB and run all seeding functions."""
    try:
        conn = sqlite3.connect(DB_NAME)
        cursor = conn.cursor()
        cursor.execute("PRAGMA foreign_keys = ON;")

        # Clear existing data to avoid conflicts
        clear_jurisdictional_tables(cursor)

        # Seed all parts of the judiciary
        seed_ordinary_judiciary(cursor)
        seed_administrative_judiciary(cursor)
        seed_commercial_judiciary(cursor)

        conn.commit()
        print("\nDatabase seeding completed successfully!")

    except sqlite3.Error as e:
        print(f"An error occurred: {e}")
        if conn:
            conn.rollback()
            print("Transaction rolled back.")
    finally:
        if conn:
            conn.close()
            print("Database connection closed.")

if __name__ == '__main__':
    main()
