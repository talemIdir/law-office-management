def clear_jurisdictional_tables(cursor):
    """Clears all data from jurisdictional tables to prevent duplicates on re-run."""
    print("Clearing old jurisdictional data...")
    # The order is important due to foreign key constraints
    cursor.execute("DELETE FROM first_degree_courts;")
    cursor.execute("DELETE FROM judicial_councils;")
    cursor.execute("DELETE FROM administrative_courts;")
    cursor.execute("DELETE FROM administrative_appeal_courts;")
    cursor.execute("DELETE FROM specialized_commercial_courts;")
    
    # Reset autoincrement counters for all affected tables
    tables_to_reset = [
        'first_degree_courts', 'judicial_councils', 
        'administrative_courts', 'administrative_appeal_courts', 
        'specialized_commercial_courts'
    ]
    for table in tables_to_reset:
        cursor.execute(f"DELETE FROM sqlite_sequence WHERE name = '{table}';")

    print("Old data cleared and sequences reset.")

